from __future__ import annotations

import argparse
import resource
import sys
import time
from dataclasses import dataclass
from pathlib import Path
from typing import Callable

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT))

from cortex_runtime.gnats import (
    FaLocalCapabilityState,
    GnatSourceInput,
    InMemoryGnatPersistenceStore,
    plan_gnat_run,
    run_parallel_gnat_plan_with_persistence,
)


DOCX_MEDIA_TYPE = "application/vnd.openxmlformats-officedocument.wordprocessingml.document"
DOCX_TEXT_FIXTURE = ROOT / "tests/runtime/fixtures/sample-note.docx"


@dataclass(frozen=True)
class DocxBenchmarkRecord:
    label: str
    concurrency: int
    shards: int
    wall_ms: float
    cpu_ms: float
    max_rss_kb: int
    run_state: str
    cache_hits: int
    cache_misses: int
    cache_records_written: int

    @property
    def shards_per_second(self) -> float:
        if self.wall_ms <= 0:
            return 0.0
        return self.shards / (self.wall_ms / 1000.0)


def ready_fa_local_state(max_concurrency: int) -> FaLocalCapabilityState:
    return FaLocalCapabilityState(
        fa_local_state="ready",
        supported_contract_versions=(
            "GnatDispatchEnvelope.v1",
            "GnatRunPlan.v1",
            "GnatWorkerReceipt.v1",
        ),
        admitted_worker_types=("markdown_syntax", "plain_text_syntax", "pdf_text_syntax", "docx_text_syntax"),
        max_concurrency=max_concurrency,
        cancellation_supported=True,
    )


def build_docx_sources(shards: int) -> list[GnatSourceInput]:
    return [
        GnatSourceInput(
            DOCX_TEXT_FIXTURE,
            media_type=DOCX_MEDIA_TYPE,
            source_ref=f"docx-text-fixture-{index:03d}",
        )
        for index in range(shards)
    ]


def measure(
    label: str,
    *,
    concurrency: int,
    shards: int,
    run: Callable[[], object],
) -> DocxBenchmarkRecord:
    start_wall = time.perf_counter()
    start_cpu = time.process_time()
    result = run()
    wall_ms = (time.perf_counter() - start_wall) * 1000.0
    cpu_ms = (time.process_time() - start_cpu) * 1000.0
    max_rss_kb = int(resource.getrusage(resource.RUSAGE_SELF).ru_maxrss)
    summary = result.summary
    persistence = result.persistence
    return DocxBenchmarkRecord(
        label=label,
        concurrency=concurrency,
        shards=shards,
        wall_ms=wall_ms,
        cpu_ms=cpu_ms,
        max_rss_kb=max_rss_kb,
        run_state=str(summary.get("run_state", "unknown")),
        cache_hits=persistence.cache_hits,
        cache_misses=persistence.cache_misses,
        cache_records_written=persistence.cache_records_written,
    )


def run_benchmark(*, shards: int, concurrency: int) -> list[DocxBenchmarkRecord]:
    sources = build_docx_sources(shards)
    store = InMemoryGnatPersistenceStore()
    cold_plan = plan_gnat_run(
        sources,
        request_id="gnat-docx-benchmark-cold",
        requested_concurrency=concurrency,
        max_concurrency=concurrency,
    )
    warm_plan = plan_gnat_run(
        sources,
        request_id="gnat-docx-benchmark-warm",
        requested_concurrency=concurrency,
        max_concurrency=concurrency,
    )

    return [
        measure(
            "docx text parallel extraction",
            concurrency=concurrency,
            shards=shards,
            run=lambda: run_parallel_gnat_plan_with_persistence(
                cold_plan,
                ready_fa_local_state(concurrency),
                store,
            ),
        ),
        measure(
            "docx text warm-cache rehydration",
            concurrency=concurrency,
            shards=shards,
            run=lambda: run_parallel_gnat_plan_with_persistence(
                warm_plan,
                ready_fa_local_state(concurrency),
                store,
            ),
        ),
    ]


def render_report(records: list[DocxBenchmarkRecord], *, memory_gate_kb: int, fixture_bytes: int) -> str:
    peak_rss = max(record.max_rss_kb for record in records)
    memory_status = "pass" if peak_rss <= memory_gate_kb else "fail"
    lines = [
        "# GNAT DOCX Lane Benchmark",
        "",
        "This report is generated by `make benchmark-gnat-docx`.",
        "The benchmark uses Cortex's bounded local DOCX lane parser.",
        "It exercises FA-Local-gated parallel dispatch and Cortex's DF-Local persistence adapter.",
        "",
        "## Host Admission",
        "",
        "- DOCX GNAT admitted: true",
        f"- fixture bytes: {fixture_bytes}",
        "",
        "| path | concurrency | shards | run state | wall ms | cpu ms | shards/sec | cache hits | cache misses | cache records written | max rss kb |",
        "|---|---:|---:|---|---:|---:|---:|---:|---:|---:|---:|",
    ]
    for record in records:
        lines.append(
            "| "
            + " | ".join(
                [
                    record.label,
                    str(record.concurrency),
                    str(record.shards),
                    record.run_state,
                    f"{record.wall_ms:.2f}",
                    f"{record.cpu_ms:.2f}",
                    f"{record.shards_per_second:.2f}",
                    str(record.cache_hits),
                    str(record.cache_misses),
                    str(record.cache_records_written),
                    str(record.max_rss_kb),
                ]
            )
            + " |"
        )

    lines.extend(
        [
            "",
            "## Acceptance Readout",
            "",
            "- DOCX shards produce schema-valid GNAT receipts under bounded FA-Local dispatch.",
            "- Warm-cache rehydration must report cache hits with zero cache misses for the same exact-version DOCX lane identity.",
            f"- Memory gate: peak RSS {peak_rss} KB <= {memory_gate_kb} KB ({memory_status}).",
            "- This lane admits bounded local DOCX structure only; review markup, corrupt packages, and nested unsupported structure remain explicit denial/degraded paths.",
            "",
        ]
    )
    return "\n".join(lines)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Benchmark the bounded Cortex GNAT DOCX lane.")
    parser.add_argument("--shards", type=int, default=4, help="number of DOCX shards to plan from the fixture")
    parser.add_argument("--concurrency", type=int, default=4, help="requested FA-Local concurrency")
    parser.add_argument("--memory-gate-kb", type=int, default=256000, help="maximum accepted ru_maxrss in KB")
    parser.add_argument("--output", type=Path, default=None, help="optional Markdown report path")
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    if args.shards < 1:
        raise SystemExit("--shards must be positive")
    if args.concurrency < 1 or args.concurrency > 8:
        raise SystemExit("--concurrency must be between 1 and 8")

    fixture_bytes = DOCX_TEXT_FIXTURE.stat().st_size
    records = run_benchmark(shards=args.shards, concurrency=args.concurrency)
    report = render_report(records, memory_gate_kb=args.memory_gate_kb, fixture_bytes=fixture_bytes)
    if args.output is not None:
        output = args.output if args.output.is_absolute() else ROOT / args.output
        output.parent.mkdir(parents=True, exist_ok=True)
        output.write_text(report, encoding="utf-8")
    print(report)
    peak_rss = max(record.max_rss_kb for record in records)
    return 0 if peak_rss <= args.memory_gate_kb else 1


if __name__ == "__main__":
    raise SystemExit(main())
